# Variables Part 3 (Strings and Function)

age = 22
print('My age is ' + str(age) + ' years.') # str() funtion convert any datatype to string.

# Replacement Fields
print('My age is {0} years.'.format(age) ) # format() use for formatting strings using multiple arguments.
print('There are {0} days in {1}, {2}, {3}, {4}, {5}, {6}, {7}, '.format(31, "January", "March", "May", "July", "August", "October", "December"))
print('Pi is approximately {0:12.50f} '.format(22/7))
